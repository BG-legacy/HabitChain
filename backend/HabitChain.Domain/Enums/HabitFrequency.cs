namespace HabitChain.Domain.Enums;

// This enum tells me how often I want to do a habit.
public enum HabitFrequency
{
    Daily = 1,   // I want to do this habit every day
    Weekly = 2,  // I want to do this habit every week
    Monthly = 3, // I want to do this habit every month
    Custom = 4   // I want to set my own custom frequency
} 